public class PatronA {
    public static void main(String[] args) {
        // Primer bloque de asteriscos
        for (int i = 0; i < 5; i++) {
            System.out.print("* ");
        }
        System.out.println();

        // Dos líneas con solo un asterisco
        for (int i = 0; i < 2; i++) {
            System.out.println("*");
        }

        // Segunda línea completa
        for (int i = 0; i < 5; i++) {
            System.out.print("* ");
        }
        System.out.println();

        // Dos líneas con solo un asterisco
        for (int i = 0; i < 2; i++) {
            System.out.println("*");
        }

        // Última línea completa
        for (int i = 0; i < 5; i++) {
            System.out.print("* ");
        }
        System.out.println();
    }
}
